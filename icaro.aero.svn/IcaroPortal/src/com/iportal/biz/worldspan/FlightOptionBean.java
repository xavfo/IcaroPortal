package com.iportal.biz.worldspan;

import java.util.ArrayList;
import java.util.List;

/**
 * Bean con opciones de vuelo para una ruta en poarticular
 * por ejemplo para ruta COCA-GUAYAQUIL
 * los segmentos son COCA-QUITO y QUITO-GUAYAQUIL
 * @author hernan
 *
 */
public class FlightOptionBean {
	
	private List<Segment> segments;
	
	
	public FlightOptionBean() {
		super();
		segments = new ArrayList<Segment>();
	}

	public List<Segment> getSegments() {
		return segments;
	}

	public void setSegments(List<Segment> segments) {
		this.segments = segments;
	}
	
	public void addSegment (Segment seg) {
		this.segments.add(seg);
	}
	
	
}
