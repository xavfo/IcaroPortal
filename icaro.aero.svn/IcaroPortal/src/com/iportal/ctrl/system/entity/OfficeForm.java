package com.iportal.ctrl.system.entity;

import javax.servlet.http.HttpServletRequest;

import org.apache.struts.action.ActionMapping;

import com.yage.Globals;
import com.yage.struts.action.BaseForm;

/**
 * Forma de toma de datos para Establecimientos
 *
 * @author ferTamayo
 * @version 1.0
 *
 */
public class OfficeForm extends BaseForm {

    private static final long serialVersionUID = -5196087707893627508L;

    private Long cityCode;

    private Long typeCode;

    private String name;

    private Boolean enabled;

    private String address;

    private String image;

    private String phone;

    private String fax;

    private String email;

    private String schedule;

    private String description;

    /**
     *
     */
    public OfficeForm() {
        super();
        this.clear();
    }

    public void clear () {
        this.cityCode=null;
        this.typeCode=null;
        this.name=null;
        this.enabled=Globals.TRUE;;
        this.address=null;
        this.image=null;
        this.phone=null;
        this.fax=null;
        this.email=null;
        this.schedule=null;
        this.description=null;
    }

    /* (non-Javadoc)
     * @see com.yage.struts.action.BaseForm#reset(org.apache.struts.action.ActionMapping, javax.servlet.http.HttpServletRequest)
     */
    @Override
    public void reset(ActionMapping mapping, HttpServletRequest request) {
        super.reset(mapping, request);
        this.clear();
    }

    /**
     * @return Returns the address.
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address The address to set.
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return Returns the cityCode.
     */
    public Long getCityCode() {
        return cityCode;
    }

    /**
     * @param cityCode The cityCode to set.
     */
    public void setCityCode(Long cityCode) {
        this.cityCode = cityCode;
    }

    /**
     * @return Returns the description.
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description The description to set.
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return Returns the email.
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email The email to set.
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return Returns the enabled.
     */
    public Boolean getEnabled() {
        return enabled;
    }

    /**
     * @param enabled The enabled to set.
     */
    public void setEnabled(Boolean enabled) {
        this.enabled = enabled;
    }

    /**
     * @return Returns the fax.
     */
    public String getFax() {
        return fax;
    }

    /**
     * @param fax The fax to set.
     */
    public void setFax(String fax) {
        this.fax = fax;
    }

    /**
     * @return Returns the image.
     */
    public String getImage() {
        return image;
    }

    /**
     * @param image The image to set.
     */
    public void setImage(String image) {
        this.image = image;
    }

    /**
     * @return Returns the name.
     */
    public String getName() {
        return name;
    }

    /**
     * @param name The name to set.
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return Returns the phone.
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone The phone to set.
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return Returns the schedule.
     */
    public String getSchedule() {
        return schedule;
    }

    /**
     * @param schedule The schedule to set.
     */
    public void setSchedule(String schedule) {
        this.schedule = schedule;
    }

    /**
     * @return Returns the typeCode.
     */
    public Long getTypeCode() {
        return typeCode;
    }

    /**
     * @param typeCode The typeCode to set.
     */
    public void setTypeCode(Long typeCode) {
        this.typeCode = typeCode;
    }










}
