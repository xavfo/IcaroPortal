package com.iportal.ctrl.system.portal;

import javax.servlet.http.HttpServletRequest;

import org.apache.struts.action.ActionMapping;

import com.yage.Globals;
import com.yage.struts.action.BaseForm;

/**
 * comment CityForm
 * 
 * @author  YAGE 
 * @version 1.1 reescrito para Carro 
 * 
 *
 */
public class CityForm extends BaseForm {

    private static final long serialVersionUID = 5284818922967311929L;

	private String name;
    
    private String key;
    
    private Long stateCode;
    
    private String stateName;
    
    private Long countryCode;
    
    private String countryName;
    
    private Boolean isEnabled;
    
    private Boolean homeDelivery;
    
    private Integer daysToDeliver;
    
    private Float  minShipCost;
    
    private Float weightCost;

    
    
    

    /**
     * Creates a new instance of SysRoleForm
     */
    public CityForm() {
        super();
    }
    
    
    public void reset(ActionMapping arg0, HttpServletRequest arg1) {
        super.reset(arg0, arg1);
        this.name = null;
        this.key = null;
        this.stateCode = null;
        this.countryCode = null;
        this.countryName = null;
        this.stateCode = null;
        this.stateName = null;
        this.isEnabled = Globals.TRUE;
        this.homeDelivery = Globals.TRUE;
        this.daysToDeliver = null;
        this.minShipCost = null;
        this.weightCost = null;
    }


	/**
	 * @return Returns the countryCode.
	 */
	public Long getCountryCode() {
		return countryCode;
	}


	/**
	 * @return Returns the countryName.
	 */
	public String getCountryName() {
		return countryName;
	}


	/**
	 * @return Returns the daysToDeliver.
	 */
	public Integer getDaysToDeliver() {
		return daysToDeliver;
	}
	

	/**
	 * @return Returns the homeDelivery.
	 */
	public Boolean getHomeDelivery() {
		return homeDelivery;
	}


	/**
	 * @return Returns the isEnabled.
	 */
	public Boolean getIsEnabled() {
		return isEnabled;
	}


	/**
	 * @return Returns the key.
	 */
	public String getKey() {
		return key;
	}


	/**
	 * @return Returns the minShipCost.
	 */
	public Float getMinShipCost() {
		return minShipCost;
	}


	/**
	 * @return Returns the name.
	 */
	public String getName() {
		return name;
	}
	
	

	/**
	 * @return Returns the stateCode.
	 */
	public Long getStateCode() {
		return stateCode;
	}


	/**
	 * @return Returns the stateName.
	 */
	public String getStateName() {
		return stateName;
	}


	/**
	 * @return Returns the weightCost.
	 */
	public Float getWeightCost() {
		return weightCost;
	}


	/**
	 * @param countryCode The countryCode to set.
	 */
	public void setCountryCode(Long countryCode) {
		this.countryCode = countryCode;
	}


	/**
	 * @param countryName The countryName to set.
	 */
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}


	/**
	 * @param daysToDeliver The daysToDeliver to set.
	 */
	public void setDaysToDeliver(Integer daysToDeliver) {
		this.daysToDeliver = daysToDeliver;
	}
	

	/**
	 * @param homeDelivery The homeDelivery to set.
	 */
	public void setHomeDelivery(Boolean homeDelivery) {
		this.homeDelivery = homeDelivery;
	}


	/**
	 * @param isEnabled The isEnabled to set.
	 */
	public void setIsEnabled(Boolean isEnabled) {
		this.isEnabled = isEnabled;
	}


	/**
	 * @param key The key to set.
	 */
	public void setKey(String key) {
		this.key = key;
	}


	/**
	 * @param minShipCost The minShipCost to set.
	 */
	public void setMinShipCost(Float minShipCost) {
		this.minShipCost = minShipCost;
	}


	/**
	 * @param name The name to set.
	 */
	public void setName(String name) {
		this.name = name;
	}

	

	/**
	 * @param stateCode The stateCode to set.
	 */
	public void setStateCode(Long stateCode) {
		this.stateCode = stateCode;
	}


	/**
	 * @param stateName The stateName to set.
	 */
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}


	/**
	 * @param weightCost The weightCost to set.
	 */
	public void setWeightCost(Float weightCost) {
		this.weightCost = weightCost;
	}
    
    
    
}
